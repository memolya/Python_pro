def same_parity(numbers):
    # Проверяем, что список не пуст
    if not numbers:             # В Python пустые коллекции (списки, кортежи, словари, множества и т.д.) считаются ложными при проверке их логического значения. Таким образом:
                                # Пустой список ([]) оценивается как False.
                                # Непустой список (например, [1, 2, 3]) оценивается как True.
        return []

    # Определяем четность первого числа
    parity = numbers[0] % 2  # 0 для четного, 1 для нечетного

    # Формируем список элементов с той же четностью
    same_parity_list = [i for i in numbers if i % 2 == parity]

    return same_parity_list

# Пример вызова
print(same_parity([]))
